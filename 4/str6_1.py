msg1 = 'ねこ いぬ たぬき'
msg2 = 'さくら|もも|うめ|ききょう'

print(msg1.split())         # 結果：['ねこ', 'いぬ', 'たぬき']
print(msg2.split('|'))      # 結果：['さくら', 'もも', 'うめ', 'ききょう']
print(msg2.rsplit('|'))     # 結果：['さくら', 'もも', 'うめ', 'ききょう']
print(msg2.split('|', 2))   # 結果：['さくら', 'もも', 'うめ|ききょう']
print(msg2.rsplit('|', 2))  # 結果：['さくら|もも', 'うめ', 'ききょう']
